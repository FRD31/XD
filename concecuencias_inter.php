        <center> 
            <br> 
            <font size="4">
        <p>Las sustancias psicoactivas, mejor conocidas como drogas, son sustancias químicas, de origen</p>
        <p>natural o sintético, que afectan las funciones del sistema nervioso central, es decir, que afectan el cerebro y la médula espinal.</p>
        <p>Los depresores también se conocen como hipnóticos o sedantes. Estos fármacos desaceleran</p>
        <p>e inhiben la actividad neurológica, además, producen fatiga en el usuario. El alcohol, los</p>
        <p>antihistamínicos, los barbitúricos y la droga de prescripción Valium son ejemplos de</p>
        <p>medicamentos depresores.</p>
        <p>Los estimulantes aumentan la actividad cerebral. Los más utilizados son la cafeína y la</p>
        <p>nicotina. La anfetamina y la cocaína también son estimulantes. Algunos estimulantes se usan</p>
        <p>de forma recreativa y otros, tales como los antidepresivos, tienen fines medicinales y se</p>
        <p>encuentran disponibles con receta médica.</p>
        <p>Los alucinógenos son sustancias que alteran la cognición y la percepción sensorial del usuario.</p>
        <p>La marihuana, la psilocibina y la mescalina son alucinógenos de origen natural. Las drogas</p>
        <p>como el LSD y el éxtasis son alucinógenos sintéticos.</p>
        <p>Los opiáceos alivian sensaciones dolorosas, son derivados del opio y producen efectos</p>
        <p>mediante la interacción con los receptores de opiáceos en el cerebro. La codeína y la morfina</p>
        <p>son opiáceos administrados clínicamente para tratar el dolor. La heroína es un opiáceo. Los</p>
        <p>opiáceos son altamente adictivos.</p>

        <img src="https://www.comparta.com.co/fotosnoticias/thumb_noticia_84.jpg"  width="400" height="200"> <br>
