        <hr color="#3BFF00">
        <font size="5">
        <b><p>1. Desajustes neuroquímicos en el cerebro</p></b>
        </font>
        <font size="4">
        <p>El consumo de drogas produce cambios neuroquímicos y funcionales permanentes en el</p>
        <p>cerebro de los adictos. Causa problemas de motivación, en la memoria y el control</p>
        <p>cognitivo. Además, los estudios demuestran que también, se produce pérdida neuronal,</p>
        <p>problemas neurodegenerativos, disminución de proteínas ácidas y otras consecuencias</p>
        <p>que afectan directamente al cerebro.</p>
        </font>
        <hr color="#3BFF00">
        <font size="5">
        <b><p>2. Alteración del humor</p></b>
        </font> 
        <font size="4">
        <p>Las alteraciones del humor son frecuentes con el consumo de droga. No solamente a largo</p>
        <p>plazo, sino también a corto plazo, lo cual significa que una persona puede pasar de estar</p>
        <p>relajada a sentirse irritada y agresiva de manera fugaz. A largo plazo, la personalidad de</p>
        <p>muchas personas puede verse alterada con el consumo de sustancias psicoactivas.</p>

        <hr color="#3BFF00">
        <font size="5">
        <b><p>3. Problemas familiares, relacionales y sociales</p></b>
        </font>
        <font size="4">
        <p>Los problemas a nivel familiar son bastante frecuentes independientemente del tipo de</p>
        <p>drogas que se consuman. Los problemas sociales también pueden presentarse cuando</p>
        <p>una persona es consumidora habitual de estas sustancias, pudiendo perder amistades e</p>
        <p>incluso el trabajo. La persona que consume drogas puede mostrar una pérdida de interés</p>
        <p>en sus aficiones y otras actividades favoritas. Los problemas económicos también se</p>
        <p>asocian a las conductas de drogadicción.</p>

        <hr color="#3BFF00">
        <font size="5">
        <b><p>4. Adicción</p></b>
        </font> 
        <p>La adicción es una de las principales consecuencias del consumo de drogas. En el caso de</p>
        <p>las drogas duras, la adicción puede provocar que una persona haga lo que esté en sus</p>
        <p>manos para obtener la sustancia que tanto quiere y necesita, llegando incluso a robar o a</p>
        <p>tener relaciones sexuales por dinero o por una dosis</p>
        <br>
        <hr color="#3BFF00"> 
        <font size="5">
        <b><p>5. Problemas cardiovasculares</p></b> 
        </font>
        <font size="4">
        <p>Los problemas cardiovasculares son habituales con el consumo prolongado de la gran</p>
        <p>mayoría de drogas, pues estas sustancias alteran el funcionamiento normal del corazón</p>
        <p>Mientras algunos fármacos provocan una actividad muy alta de este órgano, otras hacen</p>
        <p>todo lo contrario. En ambos casos se produce una variación de la presión arterial.</p>
        <p>Abusar de las drogas puede conducir a ataques cardíacos, infecciones en los vasos</p>
        <p>sanguíneos y otros problemas cardiovasculares.</p>
        <br> 
        <hr color="#3BFF00">
        <font size="5">
        <b><p>6. Debilitamiento del sistema inmune</p></b>
        </font>
        <font size="4">
        <p>Los estudios muestran, que la mayoría de las drogas debilitan el sistema inmunológico</p>
        <p>Muchos fármacos como los opiáceos que funcionan como analgésicos, provocan una serie</p>
        <p>de reacciones negativas en el sistema inmune, lo que causa que los consumidores sean</p>
        <p>más susceptibles a enfermedades e infecciones de todo tipo.</p>
        <p>Las conductas asociadas al consumo de drogas, además, pueden provocar otras</p>
        <p>enfermedades, como Sida o Hepatitis en el caso de inyectarse heroína, y llevan a realizar</p>
        <p>conductas temerarias que pueden causar otro tipo de consecuencias perjudiciales.</p>
