            <b> <p> CANNABIS </p> </b>  
        <img src="https://encrypted-tbn0.gstatic.com/images?q=tbn:ANd9GcS_Pkexilb5Fh3IVKugxBjDRBH8Do0CRA5sTA&usqp=CAU" width="400" height="200"> 
