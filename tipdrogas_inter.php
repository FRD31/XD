        <font size="6">
            <p> <b> Éxtasis </b> </p> 
            </font> 
        <img src="http://www.drugs.ie/images/uploads/banners/banner_cannabis.jpg" width="400" height="200">

             <font size="4">
        <p>Éxtasis:El éxtasis se suele elaborar en laboratorios clandestinos de algunos países europeos. Normalmente se vende en pastillas que tienen dibujados diversos logotipos o diseños. En ocasiones las pastillas de éxtasis pueden contener otras drogas y sustancias.</p>
    
        <p>Efectos – El éxtasis puede hacer que te sientas más alerta y sintonizado con el ambiente. Te sientes feliz y tranquilo, con una sensación de cariño hacia otras personas. Los sonidos, colores y emociones son más intensos. Tienes más energía, lo que te permite bailar durante períodos más largos de tiempo.</p>
       

        <p>Efectos secundarios – Tu temperatura corporal, tensión arterial y ritmo cardíaco aumentan. Otros efectos físicos incluyen dolores musculares, náuseas, agarrotamiento de la mandíbula y rechinar de dientes. Puedes experimentar sudor profuso, temblores y palpitaciones. Puedes sentirte deshidratado, confuso y cansado.</p>
        
        
        <p>Riesgos – La mayoría de las drogas legales se prueban primero en animales, pero los consumidores de éxtasis son los conejillos de indias. Las investigaciones muestran que los consumidores habituales de fin de semana experimentan una ‘crisis’ a mitad de semana, que los deja muy cansados y deprimidos, a menudo durante días. Pasarán años antes de que conozcamos los efectos a largo plazo, pero algunos consumidores pueden correr el riesgo de desarrollar problemas de salud mental posteriormente en su vida. Las muertes por éxtasis son bastante raras, pero pueden ser debidas a insolaciones, infartos o crisis de asma.</p>

        <hr color="#3BFF00"> </hr> 
        
        <p> <b> HEROÍNA </b> </p>
        
        <img src="http://www.drugs.ie/images/uploads/banners/banner_ecstasy.jpg" width="400" height="200">
        <p>Heroína: La heroína se elabora a partir de la morfina, una de las drogas que procede de la adormidera del opio</p>

        <p>Efectos – La inyección de heroína produce una ráfaga rápida de excitación seguida de una sensación de paz como en un sueño. Te sientes cariñoso, relajado y somnoliento. Los dolores, la agresión y la libido se ven reducidos.</p>
        
        <p>Efectos secundarios – Entre los efectos secundarios de la heroína y otros opiatos (como la morfina y la metadona) se encuentran el estreñimiento y el debilitamiento de la respiración. Sin embargo, la mayoría de los peligros que acarrea la heroína provienen de la sobredosis o la inyección de la droga.</p>
        
        <p>Riesgos – Lo que se vende en la calle, como la heroína, a menudo contiene otras sustancias, como el azúcar, la harina, los polvos de talco y otras drogas. Estas sustancias pueden parecer inofensivas, pero pueden provocar lesiones enormes en el cuerpo, como coágulos de sangre, abscesos y gangrena, si se inyectan. El SIDA y la hepatitis B y C se pueden contagiar si se comparten las agujas. Consumir heroína de forma frecuente puede crear adicción, en especial si se inyecta la droga.</p>
        <hr color="#3BFF00"> </hr> 
        <font size="6">
            <b> <p> METADONA </p> </b>
            </font> 
            <font size="4">
        <p>La metadona se utiliza como droga de sustitución para tratar la dependencia a la heroína. Viene en la forma de líquido verde, el cual se bebe una vez al día para evitar sentir el síndrome de abstinencia de la heroína. El riesgo más grave de la metadona es la muerte por sobredosis.</p>
        
        
