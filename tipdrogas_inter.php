        <p>Cannabis: El cannabis es una planta natural que se utiliza en tres formas principales. El tipo más común se llama la resina, que viene en bloques o trozos sólidos de color oscuro. El menos común son las hojas y los tallos de la planta, llamado hierba o maría.</p>
        
        <p>El tercer tipo, aceite de cannabis, apenas se ve en Irlanda.El cannabis se suele liar con tabaco formando un ‘porro’ o ‘canuto’ y fumar Efectos – ‘Colocarse’ con cannabis te hace sentir relajado, hablador y feliz. Hay personas que sienten que el tiempo va más despacio y también hablan de una apreciación mayor de los colores,sonidos y sabores. Se puede sentir un apetito desmedido o hambre feroz llamado en inglés ‘the munchies’.</p>
        
        
        <p>Efectos secundarios – El cannabis puede afectar a la memoria y a la concentración y puede dejarte cansado y sin motivación. Si no estás acostumbrado al cannabis o tomas un tipo más fuerte al que estás habituado, puedes sentir ansiedad, pánico o confusión. O tener delirios o alucinaciones.</p>
        <p>Riesgos – Muchas personas consideran el cannabis una droga bastante segura. Sin embargo, las investigaciones demuestran que los consumidores a largo plazo pueden tener dificultades para controlar el consumo de la droga y pueden convertirse en adictos. Fumar cannabis aumenta el riesgo de enfermedades cardíacas y cánceres, como el de pulmón, y puede afectar a la fertilidad. En personas que tienen problemas de salud mental subyacentes, el cannabis puede desencadenar la esquizofrenia. En Irlanda es la segunda droga más común hallada en los sistemas de los conductores borrachos, después del alcohol.</p>
